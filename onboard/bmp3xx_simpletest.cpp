#include "Adafruit_BMP3XX.h"
#include "common.h"

#define BMP_SCK 13
#define BMP_MISO 12
#define BMP_MOSI 11
#define BMP_CS 10

#define SEALEVELPRESSURE_HPA (1013.25)

Adafruit_BMP3XX bmp;

void setup() {
  DPRINTLN("Adafruit BMP388 / BMP390 test");

  if (!bmp.begin_I2C()) {   // hardware I2C mode, can pass in address & alt Wire
  //if (! bmp.begin_SPI(BMP_CS)) {  // hardware SPI mode  
  //if (! bmp.begin_SPI(BMP_CS, BMP_SCK, BMP_MISO, BMP_MOSI)) {  // software SPI mode
    DPRINTLN("Could not find a valid BMP3 sensor, check wiring!");
    while (1);
  }

  // Set up oversampling and filter initialization
  bmp.setTemperatureOversampling(BMP3_OVERSAMPLING_8X);
  bmp.setPressureOversampling(BMP3_OVERSAMPLING_4X);
  bmp.setIIRFilterCoeff(BMP3_IIR_FILTER_COEFF_3);
  bmp.setOutputDataRate(BMP3_ODR_50_HZ);
}

void loop() {
  if (! bmp.performReading()) {
    DPRINTLN("Failed to perform reading :(");
    return;
  }
  DPRINT("Temperature = ");
  DPRINT(bmp.temperature);
  DPRINTLN(" *C");

  DPRINT("Pressure = ");
  DPRINT(bmp.pressure / 100.0);
  DPRINTLN(" hPa");

  DPRINT("Approx. Altitude = ");
  DPRINT(bmp.readAltitude(SEALEVELPRESSURE_HPA));
  DPRINTLN(" m");

  DPRINTLN("");
  //TODO: delay(2000);
}

int main() {
  setup();
  while (1) { loop(); }
  return 0;
}
